export function narutoVolumeMeta(vol: number): { arc: string; saga: string } {
  for (const [from, to, arc, saga] of NARUTO_ARC_RANGES) {
    if (vol >= from && vol <= to) return { arc, saga };
  }
  return { arc: "Coming soon", saga: "Upcoming" };
}
